constexpr const int16_t MAX_PIXELS_PER_FRAME = 12;

BootUpScreen::BootUpScreen() :
    Clock_I(10)
{
    int16_t width = MYUCG->getDisplayWidth()/2;
    int16_t height = MYUCG->getDisplayHeight()/2;

    // offset to pic
    x_offset = width-LOGO_WIDTH/2;
    y_offset = height-LOGO_HEIGHT/2;

    MYUCG->setColor(COLOR_WHITE);

    if ( gflags.schedule_reboot ) {
        // factory first boot, do not show the logo animation
        return;
    }

    // paint logo
    MYUCG->startBuffering(x_offset, y_offset, LOGO_WIDTH+1, LOGO_HEIGHT+1);
    for (int16_t y = 0; y < LOGO_HEIGHT; y++) {
        for (int16_t x = 0; x < LOGO_WIDTH; x++) {
            int byte = logo_bitmap[y*BYTE_PER_LINE + x/4];
            int pix = (byte >> (6 - (x % 4) * 2)) & 0x03; // 2 bit per pixel
            ucg_color_t c = logo_palette[pix];
            if (x<=10 && y<=8 && pix!=0) {
                // extra pointer color for the "indicator" corner
                c = {COLOR_POINTER};
                if ( pix < 3 ) {
                    c.fadeTo(c, logo_palette[pix].r / 255.f);
                }
            }
            MYUCG->drawPixelColor(x + x_offset, y + y_offset, c);
        }
    }
    MYUCG->finishBuffering();

    Clock::start(this);
}

BootUpScreen *BootUpScreen::create()
{
    if ( ! inst ) {
        inst = new BootUpScreen();
    }
    return inst;
}

void BootUpScreen::terminate()
{
    if ( inst ) {
        BootUpScreen *tmp = inst;
        inst = nullptr;
        delete tmp;
        // MYUCG->setColor(COLOR_BLACK);
        // MYUCG->clearScreen();
    }
}

BootUpScreen::~BootUpScreen()
{
    Clock::stop(this);
}

// not much meaning left here
void BootUpScreen::finish(int16_t part)
{
    if ( gflags.schedule_reboot ) {
        SetupMenuDisplay bm("", show_boot_log);
        bm.display(part+1); // factory only case .. no display synch here
        return;
    }
}

void BootUpScreen::draw()
{
    if ( inst ) {
        inst->animate();
    }
}

void BootUpScreen::animate()
{
    ucg_color_t c;
    _fadein += 0.1f;
    c.fadeTo(ucg_color_t(COLOR_PURPLE), _fadein);
    MYUCG->setColor(c.r, c.g, c.b);
    MYUCG->setFont(ucg_font_fub20_hr);
    MYUCG->setPrintPos(MYUCG->getDisplayWidth()/2 - 22, MYUCG->getDisplayHeight()/2+LOGO_HEIGHT/2 + 34);
    MYUCG->print("pur");
    MYUCG->setColor(COLOR_WHITE);
}


bool BootUpScreen::tick()
{
    int evt = ScreenEvent(ScreenEvent::BOOT_SCREEN).raw;
    xQueueSend(uiEventQueue, &evt, 0);
    if(_fadein > 0.85f) {
        return true; // animation finished, trigger next step in boot process
    }
    return false;
}
